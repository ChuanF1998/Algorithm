#include <iostream>
using namespace std;

long wrong[21];
long all[21];

class solution
{
public:
	void Get()
	{
		wrong[0] = 0;
		wrong[1] = 0;
		wrong[2] = 1;
		all[0] = 0;
		all[1] = 1;
		all[2] = 2;
		for (int i = 3; i <= 20; ++i) {
			wrong[i] = (i - 1) * (wrong[i - 1] + wrong[i - 2]);
			all[i] = i * all[i - 1];
		}
		return;
	}
};

int main()
{
	solution ss;
	ss.Get();
	int n;
	while (cin >> n) {
		double result = wrong[n] / (all[n] * 1.0);
		result *= 100;
		cout.setf(ios::fixed);
		cout.precision(2);
		cout << result << "%" << endl;
	}
	return 0;
}